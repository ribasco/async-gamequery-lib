package com.ibasco.agql.examples.rcon;

import java.util.HashSet;
import java.util.Set;

public class SMPlugin {

    private final String id;

    private final String name;

    private String version;

    private String author;

    private String filename;

    private String title;

    private String url;

    private String status;

    private String timestamp;

    private String hash;

    private final Set<SMConVar> conVars = new HashSet<>();

    private final Set<SMCommand> commands = new HashSet<>();

    public SMPlugin(String id, String name, String version, String author) {
        this.id = id;
        this.name = name;
        this.version = version;
        this.author = author;
    }

    public Set<SMConVar> getConVars() {
        return conVars;
    }

    public Set<SMCommand> getCommands() {
        return commands;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getAuthor() {
        return author;
    }

    public String getFilename() {
        return filename;
    }

    public void setFilename(String filename) {
        this.filename = filename;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(String timestamp) {
        this.timestamp = timestamp;
    }

    public String getHash() {
        return hash;
    }

    public void setHash(String hash) {
        this.hash = hash;
    }
}